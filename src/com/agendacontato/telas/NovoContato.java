    private void configComponentes() {
        TipoOpcoes[] values = TipoOpcoes.values();
        for (TipoOpcoes value : values) {
            jCTipo.addItem(value.name());
        }
    }

    private void limparCampos() {
        jTNome.setText("");
        JTTelefone.setText("");
        jCTipo.setSelectedIndex(0);

    }

    void quandoSalvarContato(Thread quandoExecutar) {
        this.quandoExecutar = quandoExecutar;
    }

    private int pegarTipoSelecionado() {
        String tipoSelecionado = jCTipo.getSelectedItem().toString();
        int tipo = 1;
        switch (tipoSelecionado) {
            case "FAMILIA":
                tipo = TipoOpcoes.FAMILIA.getValor();
                break;
            case "TRABALHO":
                tipo = TipoOpcoes.TRABALHO.getValor();
                break;
            case "OUTROS":
                tipo = TipoOpcoes.OUTROS.getValor();
                break;
        }
        return tipo;
    }

