    private void JBSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBSalvarActionPerformed
        try {
            String nome = jTNome.getText();
            String telefone = JTTelefone.getText();
            int tipo = pegarTipoSelecionado();

            if (nome.isEmpty() || JTTelefone.getValue() == null) {
                JOptionPane.showMessageDialog(this, "Preencha todos os campos.",
                        "Erro", JOptionPane.ERROR_MESSAGE);
            } else {
                Contato contato = new Contato(nome, telefone, tipo);
                ContatoAD contatoAD = new ContatoAD(Conexao.pegarConexao());
                contatoAD.salvar(contato);
                limparCampos();
                this.quandoExecutar.start();
                JOptionPane.showMessageDialog(this, "Salvo!", "Aviso", JOptionPane.INFORMATION_MESSAGE);
            }

        } catch (ClassNotFoundException | SQLException | NullPointerException ex) {

        }
    }//GEN-LAST:event_JBSalvarActionPerformed

